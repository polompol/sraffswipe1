// простым (меньше отвлекающего для первых пользователей и оператора). Код
// остаётся — включается обратно сменой VITE_PILOT_MODE в .env. Ничего не удалено.
//
// Что прячем при VITE_PILOT_MODE=true:
//  - покупка Boost (поднятие в топ) — некуда поднимать при пустой ленте;
//  - верификация по ИНН (DaData) — первые кафе приводятся лично;
//  - алерты по сохранённым поискам — нужны при десятках вакансий.
// По умолчанию ВКЛЮЧЁН (пилот — простое ядро). Полный набор фич возвращается
// явным VITE_PILOT_MODE=false. Так на старте приложение сразу без лишнего.
export const PILOT_MODE = import.meta.env.VITE_PILOT_MODE !== "false";
